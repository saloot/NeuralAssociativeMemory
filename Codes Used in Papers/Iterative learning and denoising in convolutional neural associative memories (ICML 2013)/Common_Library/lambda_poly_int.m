%==========================================================================
%***********************FUNCTION: lambda_poly_int**************************
%==========================================================================

%--------------------------------INPUTS------------------------------------
% x: The point we would like to evaluate the function
% lambda: The degree distribution coefficients
%--------------------------------------------------------------------------

%--------------------------------OUTPUTS-----------------------------------
% f: The value of the function
%--------------------------------------------------------------------------

%--------------------------FUNCTION DESCRIPTION----------------------------
% This function calculates the value of the integral of the pecified degree 
% distibution polynomial.
%--------------------------------------------------------------------------

%==========================================================================
%==========================================================================

function f = lambda_poly_int(x,lambda)
f = zeros(1,length(x));
for i = 1:length(lambda)
    f = f+ lambda(i)*x.^(i+1)/(i+1);                      % Note the power 
end