%==========================================================================
%************************FUNCTION: rho_poly_der****************************
%==========================================================================

%--------------------------------INPUTS------------------------------------
% x: The point we would like to evaluate the function
% rho: The degree distribution coefficients
%--------------------------------------------------------------------------

%--------------------------------OUTPUTS-----------------------------------
% f: The value of the function
%--------------------------------------------------------------------------

%--------------------------FUNCTION DESCRIPTION----------------------------
% This function calculates the value of the derivative of the pecified 
% degree distibution polynomial.
%--------------------------------------------------------------------------

%==========================================================================
%==========================================================================

function f = rho_poly_der(x,rho)
f = zeros(1,length(x));
for i = 1:length(rho)
    f = f+ (i-1)*rho(i)*x.^(i-2);                      % Note the power 
end