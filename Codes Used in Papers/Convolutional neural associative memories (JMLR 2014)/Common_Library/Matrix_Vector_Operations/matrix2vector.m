%==========================================================================
%************************FUNCTION: matrix2vector***************************
%==========================================================================

%--------------------------------INPUTS------------------------------------
% A: The input matrix
%--------------------------------------------------------------------------

%--------------------------------OUTPUTS-----------------------------------
% v: The output vector
%--------------------------------------------------------------------------


%--------------------------FUNCTION DESCRIPTION----------------------------
% This function gets a matrix as the input and transform it to a vector by
% concatenating rows of the matrix together. 
%--------------------------------------------------------------------------

%==========================================================================
%==========================================================================

function v = matrix2vector(A)
%============================INITIALIZATIONS===============================
[m,~] = size(A);
v = [];
%==========================================================================

%===============================MAIN LOOP==================================
for i = 1:m
    v = [v,A(i,:)];
end
%==========================================================================
