%==========================================================================
%************************FUNCTION: vector2matrix***************************
%==========================================================================

%--------------------------------INPUTS------------------------------------
% v: The input vector
% n: The number of columns in the output matrix
%--------------------------------------------------------------------------

%--------------------------------OUTPUTS-----------------------------------
% A: The output matrix
%--------------------------------------------------------------------------


%--------------------------FUNCTION DESCRIPTION----------------------------
% This function gets a vector and generates a matrix in which each row 
% is composed of the consecutive n elements of the input vector.
%--------------------------------------------------------------------------

%==========================================================================
%==========================================================================

function A = vector2matrix(v,n)

%============================INITIALIZATIONS===============================
m = length(v)/n;					% This is the number of rows in the output matrix
A = [];

%-----------------Check the Validity of the Input Parameters---------------
if (abs(m - round(m) )>0)
    error('Invalid vector/matrix sizes');
end
%--------------------------------------------------------------------------

%==========================================================================

%===============================MAIN LOOP==================================
for i = 1:m
    A = [A;v((i-1)*n+1:i*n)];
end
%==========================================================================
